    "ttkbootstrap",
    "paramiko",
    "sqlalchemy",
    "cryptography",
    "apscheduler",
    "pyinstaller",
    "pillow"
)

$missingPackages = @()
foreach ($package in $requiredPackages) {
    if ($pipList -notmatch "^$package==") {
        $missingPackages += $package
    }
}

